
public class DecrementAndIncrement {

	public static void main(String[] args) {
		int counter = 5;
		counter++;
		System.out.println(counter);
		counter = 5;
		counter = counter + 1;
		System.out.println(counter);
		counter = 5;
		counter--;
		System.out.println(counter);
		counter = 5;
		counter = counter - 1;
		System.out.println(+counter);
	}

}
